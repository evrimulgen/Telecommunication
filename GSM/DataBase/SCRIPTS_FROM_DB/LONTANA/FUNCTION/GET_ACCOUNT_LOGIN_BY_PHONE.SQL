
  CREATE OR REPLACE FUNCTION "LONTANA"."GET_ACCOUNT_LOGIN_BY_PHONE" (
  pPHONE_NUMBER IN VARCHAR2
  ) RETURN varchar2 IS
--Vesion=1
CURSOR C IS
  SELECT ACCOUNTS.LOGIN
    FROM DB_LOADER_ACCOUNT_PHONES,
         ACCOUNTS
    WHERE DB_LOADER_ACCOUNT_PHONES.PHONE_NUMBER=pPHONE_NUMBER
      AND DB_LOADER_ACCOUNT_PHONES.ACCOUNT_ID=ACCOUNTS.ACCOUNT_ID(+)
    ORDER BY DB_LOADER_ACCOUNT_PHONES.LAST_CHECK_DATE_TIME DESC;
DUMMY varchar2(20);
ITOG varchar2(20);
BEGIN
  OPEN C;
  FETCH C INTO DUMMY;
  IF C%FOUND THEN
    ITOG:=DUMMY;
  ELSE
    ITOG:=0;
  END IF;
  CLOSE C;
  RETURN ITOG;
END;
