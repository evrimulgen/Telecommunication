
  CREATE OR REPLACE FUNCTION "LONTANA"."GET_LOGIN_BY_PHONE" (
  pPHONE_NUMBER IN VARCHAR2
  ) RETURN varchar2 IS
--Vesion=1
CURSOR C IS
  SELECT acs.login
    FROM DB_LOADER_ACCOUNT_PHONES, accounts acs
    WHERE DB_LOADER_ACCOUNT_PHONES.PHONE_NUMBER=pPHONE_NUMBER
    and acs.account_id=DB_LOADER_ACCOUNT_PHONES.ACCOUNT_ID
    ORDER BY DB_LOADER_ACCOUNT_PHONES.LAST_CHECK_DATE_TIME DESC;
DUMMY varchar2(30);
ITOG varchar2(30);
BEGIN
  OPEN C;
  FETCH C INTO DUMMY;
  IF C%FOUND THEN
    ITOG:=DUMMY;
  ELSE
    ITOG:='';
  END IF;
  CLOSE C;
  RETURN ITOG;
END;