
  CREATE OR REPLACE TRIGGER "LONTANA"."TIU_BEELINE_BS_ZONES" 
  BEFORE INSERT OR UPDATE ON BEELINE_BS_ZONES FOR EACH ROW
--#Version=1
BEGIN
  IF INSERTING THEN
    :NEW.USER_CREATED := USER;
    :NEW.DATE_CREATED := SYSDATE;
  END IF;
  :NEW.USER_LAST_UPDATED := USER;
  :NEW.DATE_LAST_UPDATED := SYSDATE;
END; 
ALTER TRIGGER "LONTANA"."TIU_BEELINE_BS_ZONES" ENABLE