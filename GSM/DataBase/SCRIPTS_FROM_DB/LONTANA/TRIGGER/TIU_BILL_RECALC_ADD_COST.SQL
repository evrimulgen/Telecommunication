
  CREATE OR REPLACE TRIGGER "LONTANA"."TIU_BILL_RECALC_ADD_COST" 
  BEFORE INSERT OR UPDATE ON BILL_RECALC_ADD_COST FOR EACH ROW
--#Version=1
BEGIN
  IF UPDATING THEN
    IF (:NEW.CALLS_ALL < :OLD.CALLS_ALL) OR (:NEW.CALLS < :OLD.CALLS) OR (:NEW.SMS < :OLD.SMS) THEN
      :NEW.CALLS_ALL := :OLD.CALLS_ALL;
      :NEW.CALLS := :OLD.CALLS;
      :NEW.SMS := :OLD.SMS;
    END IF;
  END IF;
  :NEW.DATE_CHECK := SYSDATE;
END; 
ALTER TRIGGER "LONTANA"."TIU_BILL_RECALC_ADD_COST" ENABLE