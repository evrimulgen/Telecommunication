
  CREATE OR REPLACE TRIGGER "LONTANA"."TIU_CONTRACT_DOP_STATUSES" 
  BEFORE INSERT OR UPDATE ON CONTRACT_DOP_STATUSES FOR EACH ROW
BEGIN
  IF INSERTING THEN
    IF NVL(:NEW.DOP_STATUS_ID, 0) = 0 THEN
      :NEW.DOP_STATUS_ID := NEW_DOP_STATUS_ID;
    END IF;
    :NEW.USER_CREATED := USER;
    :NEW.DATE_CREATED := SYSDATE;
  END IF;
  :NEW.USER_LAST_UPDATED := USER;
  :NEW.DATE_LAST_UPDATED := SYSDATE;
END; 
ALTER TRIGGER "LONTANA"."TIU_CONTRACT_DOP_STATUSES" ENABLE