
  CREATE OR REPLACE TRIGGER "LONTANA"."TIU_CRM_EVENTS" 
  BEFORE INSERT OR UPDATE ON CRM_EVENTS FOR EACH ROW
--#Version=1
BEGIN
  IF INSERTING THEN
    IF NVL(:NEW.EVENT_ID, 0) = 0 then
      :NEW.EVENT_ID := S_NEW_EVENTS_ID.NEXTVAL;
    END IF;
    :NEW.DATE_CREATED:=SYSDATE;
  END IF;
  :NEW.DATE_LAST_UPDATED:=SYSDATE;
END;
ALTER TRIGGER "LONTANA"."TIU_CRM_EVENTS" ENABLE