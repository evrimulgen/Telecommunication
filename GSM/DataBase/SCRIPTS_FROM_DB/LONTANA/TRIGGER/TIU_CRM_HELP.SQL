
  CREATE OR REPLACE TRIGGER "LONTANA"."TIU_CRM_HELP" 
  BEFORE INSERT OR UPDATE ON CRM_HELP FOR EACH ROW
--#Version=1
BEGIN
  IF INSERTING THEN
    IF NVL(:NEW.HELP_ID, 0) = 0 then
      :NEW.HELP_ID := S_NEW_HELP_ID.NEXTVAL;
    END IF;
    :NEW.DATE_CREATED:=SYSDATE;
  END IF;
  :NEW.DATE_LAST_UPDATED:=SYSDATE;
END;
ALTER TRIGGER "LONTANA"."TIU_CRM_HELP" ENABLE