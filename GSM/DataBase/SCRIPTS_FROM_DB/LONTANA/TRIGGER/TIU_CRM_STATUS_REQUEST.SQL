
  CREATE OR REPLACE TRIGGER "LONTANA"."TIU_CRM_STATUS_REQUEST" 
  BEFORE INSERT OR UPDATE ON CRM_STATUS_REQUEST FOR EACH ROW
--#Version=1
BEGIN
  IF INSERTING THEN
    IF NVL(:NEW.STATUS_REQUEST_ID, 0) = 0 then
      :NEW.STATUS_REQUEST_ID := S_STATUS_REQUEST.NEXTVAL;
    END IF;
  END IF;
END;
ALTER TRIGGER "LONTANA"."TIU_CRM_STATUS_REQUEST" ENABLE