
  CREATE OR REPLACE TRIGGER "LONTANA"."TIU_CRM_TYPE_REQUEST" 
  BEFORE INSERT OR UPDATE ON CRM_TYPE_REQUEST FOR EACH ROW
--#Version=1
BEGIN
  IF INSERTING THEN
    IF NVL(:NEW.TYPE_REQUEST_ID, 0) = 0 then
      :NEW.TYPE_REQUEST_ID := S_TYPE_REQUEST.NEXTVAL;
    END IF;
  END IF;
END;
