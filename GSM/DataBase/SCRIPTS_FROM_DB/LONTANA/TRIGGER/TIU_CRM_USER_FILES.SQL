
  CREATE OR REPLACE TRIGGER "LONTANA"."TIU_CRM_USER_FILES" 
  BEFORE INSERT OR UPDATE ON CRM_USER_FILES FOR EACH ROW
--#Version=1
DECLARE
  CURSOR C IS
    SELECT NVL(MAX(CRM_USER_FILE_ID), 0) + 1 AS COU
      FROM CRM_USER_FILES;
  DUMMY C%ROWTYPE; 
BEGIN
  IF INSERTING THEN
    IF NVL(:NEW.CRM_USER_FILE_ID, 0) = 0 then
      OPEN C;
      FETCH C INTO DUMMY;
      IF C%FOUND THEN
        :NEW.CRM_USER_FILE_ID := DUMMY.COU;
      ELSE
        :NEW.CRM_USER_FILE_ID := 1;
      END IF;
    END IF;
    :NEW.DATE_CREATE := SYSDATE;
  END IF;
  :NEW.DATE_LAST_UPDATE := SYSDATE;
END; 
ALTER TRIGGER "LONTANA"."TIU_CRM_USER_FILES" ENABLE