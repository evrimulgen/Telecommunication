
  CREATE OR REPLACE TRIGGER "LONTANA"."TIU_DOCUM_TYPES" 
  BEFORE INSERT OR UPDATE ON DOCUM_TYPES FOR EACH ROW
--#Version=1
BEGIN
  IF INSERTING THEN
    IF NVL(:NEW.DOCUM_TYPE_ID, 0) = 0 then
      :NEW.DOCUM_TYPE_ID := NEW_DOCUM_TYPE_ID;
    END IF;
    :NEW.USER_CREATED := USER;
    :NEW.DATE_CREATED := SYSDATE;
  END IF;
  :NEW.USER_LAST_UPDATED := USER;
  :NEW.DATE_LAST_UPDATED := SYSDATE;
END; 
ALTER TRIGGER "LONTANA"."TIU_DOCUM_TYPES" ENABLE