
  CREATE OR REPLACE TRIGGER "LONTANA"."TIU_GUARANTEE_FEES" 
BEFORE INSERT OR UPDATE
ON  GUARANTEE_FEES
REFERENCING NEW AS NEW OLD AS OLD
FOR EACH ROW
BEGIN 


  if INSERTING then
    IF NVL(:NEW.GUARANTEE_FEES_ID, 0) = 0 then 
      :NEW.GUARANTEE_FEES_ID := s_NEW_GUARANTEE_FEES_ID.NEXTVAL;
    END IF;  
    
    :NEW.USER_CREATED := USER;
    :NEW.DATE_CREATED := SYSDATE;
  end if; 
  
  if updating then     
    :NEW.USER_UPDATE := USER;
    :NEW.DATE_UPDATE := SYSDATE;
  end if;
  
END TIU_GUARANTEE_FEES;
ALTER TRIGGER "LONTANA"."TIU_GUARANTEE_FEES" ENABLE