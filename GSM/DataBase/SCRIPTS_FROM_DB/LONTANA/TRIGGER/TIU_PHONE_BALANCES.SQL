
  CREATE OR REPLACE TRIGGER "LONTANA"."TIU_PHONE_BALANCES" 
  BEFORE INSERT OR UPDATE ON PHONE_BALANCES FOR EACH ROW
--#Version=1
BEGIN
  IF INSERTING THEN
    IF NVL(:NEW.PHONE_BALANCE_ID, 0) = 0 then
      :NEW.PHONE_BALANCE_ID := NEW_PHONE_BALANCE_ID;
    END IF;
    :NEW.USER_CREATED := USER;
    :NEW.DATE_CREATED := SYSDATE;
  END IF;
  :NEW.USER_LAST_UPDATED := USER;
  :NEW.DATE_LAST_UPDATED := SYSDATE;
END; 
ALTER TRIGGER "LONTANA"."TIU_PHONE_BALANCES" ENABLE