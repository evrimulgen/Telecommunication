
  CREATE OR REPLACE TRIGGER "LONTANA"."TIU_ROAMING_RETARIF_SERVS" 
  BEFORE INSERT OR UPDATE ON ROAMING_RETARIF_SERVS FOR EACH ROW 
--#Version=1
BEGIN 
  IF INSERTING THEN 
    IF NVL(:NEW.ID, 0) = 0 then 
      :NEW.ID := S_NEW_ROAMING_RETARIF_SERV_ID.NEXTVAL; 
    END IF; 
    :NEW.USER_CREATED := USER; 
    :NEW.DATE_CREATED := SYSDATE;
  END IF; 
  :NEW.USER_LAST_UPDATED := USER; 
  :NEW.DATE_LAST_UPDATED := SYSDATE; 
END;
--#end if
ALTER TRIGGER "LONTANA"."TIU_ROAMING_RETARIF_SERVS" ENABLE