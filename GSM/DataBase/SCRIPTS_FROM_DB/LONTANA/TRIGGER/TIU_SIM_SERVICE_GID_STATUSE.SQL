
  CREATE OR REPLACE TRIGGER "LONTANA"."TIU_SIM_SERVICE_GID_STATUSE" 
  BEFORE INSERT OR UPDATE ON LONTANA.SIM_SERVICE_GID_STATUSES FOR EACH ROW
BEGIN
  IF INSERTING THEN
    IF NVL(:NEW.SIM_SG_STATUS_ID, 0) = 0 THEN
      :NEW.SIM_SG_STATUS_ID := NEW_SIM_SG_STATUS_ID;
    END IF;
    :NEW.USER_CREATED := USER;
    :NEW.DATE_CREATED := SYSDATE;
  END IF;
  :NEW.USER_LAST_UPDATED := USER;
  :NEW.DATE_LAST_UPDATED := SYSDATE;
END; 
ALTER TRIGGER "LONTANA"."TIU_SIM_SERVICE_GID_STATUSE" ENABLE