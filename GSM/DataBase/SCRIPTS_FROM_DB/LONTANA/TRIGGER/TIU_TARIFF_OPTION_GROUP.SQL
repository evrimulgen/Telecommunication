
  CREATE OR REPLACE TRIGGER "LONTANA"."TIU_TARIFF_OPTION_GROUP" 
  BEFORE INSERT OR UPDATE ON TARIFF_OPTION_GROUP FOR EACH ROW
--#Version=1
BEGIN
  IF INSERTING THEN
    IF NVL(:NEW.OPTION_GROUP_ID, 0) = 0 then
      :NEW.OPTION_GROUP_ID := NEW_OPTION_GROUP_ID;
    END IF;
    :NEW.USER_CREATED := USER;
    :NEW.DATE_CREATED := SYSDATE;
  END IF;
  :NEW.USER_LAST_UPDATED := USER;
  :NEW.DATE_LAST_UPDATED := SYSDATE;
END; 
ALTER TRIGGER "LONTANA"."TIU_TARIFF_OPTION_GROUP" ENABLE