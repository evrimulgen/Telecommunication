
  CREATE OR REPLACE TRIGGER "LONTANA"."TI_DATABASE_COPIES" 
  BEFORE INSERT ON DATABASE_COPIES FOR EACH ROW
--
--#Version=1
--
BEGIN
  IF INSERTING THEN
    IF NVL(:NEW.ID_DATABASE, 0) = 0 THEN
      :NEW.ID_DATABASE := S_NEW_ID_DATABASE.NEXTVAL;
    END IF;
    :NEW.USER_CREATED := USER;
    :NEW.DATE_CREATED := SYSDATE;
  END IF;
END;
ALTER TRIGGER "LONTANA"."TI_DATABASE_COPIES" ENABLE