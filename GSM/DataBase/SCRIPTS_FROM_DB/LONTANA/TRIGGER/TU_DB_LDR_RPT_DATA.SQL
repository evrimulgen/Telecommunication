
  CREATE OR REPLACE TRIGGER "LONTANA"."TU_DB_LDR_RPT_DATA" 
    BEFORE UPDATE
  ON DB_LOADER_REPORT_DATA  REFERENCING NEW AS NEW OLD AS OLD
   FOR EACH ROW
BEGIN
    IF UPDATING THEN  
        BEGIN     

         IF (:new.DETAIL_SUM <> :old.DETAIL_SUM) 
         THEN
           :new.DATE_LAST_UPDATE := SYSDATE;
         END IF;
    
       END;    
    
    END IF; 
         
END;
