
  CREATE OR REPLACE TRIGGER "LONTANA"."TU_DILER_CALL_FOR_PHONE" 
    BEFORE UPDATE
  ON DILER_CALL_FOR_PHONE  REFERENCING NEW AS NEW OLD AS OLD
   FOR EACH ROW
BEGIN
  IF UPDATING THEN    
    IF (:new.DILER_CALLS < :old.DILER_CALLS) then
     :new.DILER_CALLS := :old.DILER_CALLS;
    END IF;          
  END IF;          
END; 
ALTER TRIGGER "LONTANA"."TU_DILER_CALL_FOR_PHONE" ENABLE