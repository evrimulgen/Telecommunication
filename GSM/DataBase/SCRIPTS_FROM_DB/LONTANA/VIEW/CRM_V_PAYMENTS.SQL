
  CREATE OR REPLACE FORCE VIEW "LONTANA"."CRM_V_PAYMENTS" ("PHONE_NUMBER", "PAYMENT_SUM", "PAYMENT_DATE", "CONTRACT_ID", "PAYMENT_REMARK", "PAYMENT_TYPE") AS 
  SELECT
  "PHONE_NUMBER","PAYMENT_SUM","PAYMENT_DATE","CONTRACT_ID","PAYMENT_REMARK","PAYMENT_TYPE"
FROM
  V_FULL_BALANCE_PAYMENTS
WHERE
  V_FULL_BALANCE_PAYMENTS.PAYMENT_TYPE IN (1, 2)
ORDER BY
  V_FULL_BALANCE_PAYMENTS.PAYMENT_DATE DESC