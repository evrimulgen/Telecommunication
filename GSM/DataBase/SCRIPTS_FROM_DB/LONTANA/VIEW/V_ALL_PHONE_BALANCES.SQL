
  CREATE OR REPLACE FORCE VIEW "LONTANA"."V_ALL_PHONE_BALANCES" ("YEAR_MONTH", "PHONE_NUMBER", "CELL_PLAN_CODE", "TARIFF_NAME", "BALANCE_VALUE") AS 
  SELECT
--#Version=1 
  DB_LOADER_ACCOUNT_PHONES.YEAR_MONTH, 
  DB_LOADER_ACCOUNT_PHONES.PHONE_NUMBER, 
  DB_LOADER_ACCOUNT_PHONES.CELL_PLAN_CODE,
  TARIFFS.TARIFF_NAME,
  GET_ABONENT_BALANCE(DB_LOADER_ACCOUNT_PHONES.PHONE_NUMBER) BALANCE_VALUE 
FROM DB_LOADER_ACCOUNT_PHONES,
TARIFFS
WHERE TARIFFS.TARIFF_ID(+)=GET_PHONE_TARIFF_ID(
    DB_LOADER_ACCOUNT_PHONES.PHONE_NUMBER,
    DB_LOADER_ACCOUNT_PHONES.CELL_PLAN_CODE,
    DB_LOADER_ACCOUNT_PHONES.LAST_CHECK_DATE_TIME
    ) 