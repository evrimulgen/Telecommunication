
  CREATE OR REPLACE FORCE VIEW "LONTANA"."V_GUARANTEE_FEES" ("ACCOUNT_ID", "BILL_NUMBER", "DATE_CREATED", "DATE_UPDATE", "GUARANTEE_FEES_ID", "NOT_PAID", "PAID", "PHONE_NUMBER", "RETURNED", "STATUS_GUARANT_FEE", "SUM_GUARANT_FEE", "USER_CREATED", "USER_UPDATE", "WITHDRAWN", "YEAR_MONTH", "ACCOUNT_NUMBER", "LOGIN") AS 
  SELECT 
    GF.ACCOUNT_ID, 
    GF.BILL_NUMBER, 
    GF.DATE_CREATED,
    GF.DATE_UPDATE,
    GF.GUARANTEE_FEES_ID,
    GF.NOT_PAID, GF.PAID,
    GF.PHONE_NUMBER,
    GF.RETURNED,
    GF.STATUS_GUARANT_FEE,
    GF.SUM_GUARANT_FEE,
    GF.USER_CREATED,
    GF.USER_UPDATE,
    GF.WITHDRAWN,
    GF.YEAR_MONTH,
    AC.ACCOUNT_NUMBER,
    AC.LOGIN
    FROM  GUARANTEE_FEES gf, ACCOUNTS ac
  where gf.account_id = ac.account_id