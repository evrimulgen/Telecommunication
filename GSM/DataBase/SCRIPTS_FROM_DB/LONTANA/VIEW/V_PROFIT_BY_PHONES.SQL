
  CREATE OR REPLACE FORCE VIEW "LONTANA"."V_PROFIT_BY_PHONES" ("ACCOUNT_ID", "YEAR_MONTH", "PHONE_NUMBER", "OPERATOR_BILL_SUM", "CLIENT_BILL_SUM", "PROFIT_SUM") AS 
  SELECT V.ACCOUNT_ID,
          V.YEAR_MONTH,
          V.PHONE_NUMBER,
          CASE WHEN V.BILL_SUM_OLD < 0 THEN 0 ELSE V.BILL_SUM_OLD END
             AS OPERATOR_BILL_SUM,
          CASE WHEN V.BILL_SUM_OLD < 0 THEN 0 ELSE V.BILL_SUM_NEW END
             AS CLIENT_BILL_SUM,
          CASE
             WHEN V.BILL_SUM_OLD < 0 THEN 0
             ELSE V.BILL_SUM_NEW - V.BILL_SUM_OLD
          END
             AS PROFIT_SUM
     FROM V_BILL_FINANCE_FOR_CLIENTS v
     
