
  CREATE OR REPLACE FORCE VIEW "LONTANA"."V_SIM_PHONES_BILLS" ("CELL_NUMBER") AS 
  SELECT S1.CELL_NUMBER
    FROM SIM S1
    WHERE S1.SERVICEGID_STATUS<>4
      and S1.SERVICEGID_STATUS<>0
      AND NOT EXISTS
          (SELECT 1
             FROM SIM_PHONE_BILLS
             WHERE SIM_PHONE_BILLS.YEAR_MONTH=TO_NUMBER(TO_CHAR(ADD_MONTHS(SYSDATE, -1), 'YYYYMM'))
              AND SIM_PHONE_BILLS.PHONE_NUMBER=S1.CELL_NUMBER)    
      AND EXISTS
          (SELECT 1
             FROM SIM_PHONE_BILLS
             WHERE SIM_PHONE_BILLS.YEAR_MONTH>=TO_NUMBER(TO_CHAR(ADD_MONTHS(SYSDATE, -6), 'YYYYMM'))
              AND SIM_PHONE_BILLS.PHONE_NUMBER=S1.CELL_NUMBER
              and SIM_PHONE_BILLS.bill_sum <> 0 )
      AND (SELECT COUNT(*)
             FROM SIM S2
             WHERE S2.ACCOUNT = S1.ACCOUNT) <= 1
    ORDER BY S1.CELL_NUMBER DESC 