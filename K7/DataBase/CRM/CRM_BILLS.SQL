CREATE OR REPLACE FORCE VIEW CRM_V_BILLS
AS
SELECT AC.LOGIN,
       FB.*
  FROM DB_LOADER_FULL_FINANCE_BILL FB,
       ACCOUNTS AC
  WHERE FB.ACCOUNT_ID = AC.ACCOUNT_ID(+)
    AND FB.COMPLETE_BILL = 1
  ORDER BY FB.YEAR_MONTH DESC;

GRANT SELECT ON CRM_V_BILLS TO CRM_USER;
CREATE SYNONYM CRM_USER.BILLS FOR CRM_V_BILLS;
