
  CREATE OR REPLACE FUNCTION "CORP_MOBILE"."GET_MAIL_PHONE_FOR_BL_SAVE3" RETURN CLOB IS
--Version=1
RESULT_CLOB CLOB;
LINE CLOB;
BEGIN
 RESULT_CLOB:='"Phone Number for Block"'|| CHR(13) ||CHR(10) ;
  FOR rec IN(
    SELECT vv.phone_number
   FROM v_phone_number_for_block_save vv
      ) LOOP

   LINE:=CHR(13) ||CHR(10) || rec.PHONE_NUMBER;

    DBMS_LOB.APPEND(RESULT_CLOB, LINE);
  END LOOP;
  RETURN RESULT_CLOB;
  DBMS_LOB.FREETEMPORARY(RESULT_CLOB);
END;
