
  CREATE OR REPLACE PROCEDURE "CORP_MOBILE"."BEELINE_BS_ZONE_ADD_CODE" (
  pBS_CODE IN NUMBER,
  pZONE_NAME IN VARCHAR2
  ) IS
--#Version=1
BEGIN
  INSERT INTO BEELINE_BS_ZONES (BEELINE_BS_ZONE_ID, ZONE_NAME)
  VALUES (pBS_CODE, pZONE_NAME);
EXCEPTION
  WHEN DUP_VAL_ON_INDEX THEN
    UPDATE BEELINE_BS_ZONES 
      SET BEELINE_BS_ZONES.ZONE_NAME=pZONE_NAME
      WHERE BEELINE_BS_ZONES.BEELINE_BS_ZONE_ID=pBS_CODE;
END; 