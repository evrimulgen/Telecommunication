
  CREATE OR REPLACE PROCEDURE "CORP_MOBILE"."BLOCK_PHONE_WITHOUT_CONTRACTS" IS
-- Version = 5
  CURSOR C IS
    SELECT *
      FROM BLOCK_NO_CONTRACTS_SETTINGS;
  CURSOR C1 (c1PHONE_NUMBER VARCHAR2) IS
    SELECT * FROM CONTRACTS 
    WHERE CONTRACTS.PHONE_NUMBER_FEDERAL = c1PHONE_NUMBER
    AND NOT EXISTS (SELECT CONTRACT_CANCEL_DATE
                    FROM CONTRACT_CANCELS
                    WHERE CONTRACT_CANCELS.CONTRACT_ID = CONTRACTS.CONTRACT_ID);   
  LOCK_PH VARCHAR2(2000);  
  vDUMMY C%ROWTYPE;
  vCONTR C1%ROWTYPE;
  ACCESS_DENIED_LIST CLOB;

BEGIN
  OPEN C;
  FETCH C INTO vDUMMY;
  CLOSE C;
  
  ACCESS_DENIED_LIST:=GET_LIST_PHONE_NO_BL_FROM_SYTE;
  IF (vDUMMY.CHECK_TIME = 0) 
      OR ((TRUNC(SYSDATE) + vDUMMY.TIME_END/24 > SYSDATE)   --  До скольки утром работать
            OR (TRUNC(SYSDATE) + vDUMMY.TIME_BEGIN/24 < SYSDATE)) THEN  -- До скольки вечером работать
    FOR rec IN(
      SELECT V_ACTIVE_NUMBERS_OUT_CONTRACTS.PHONE_NUMBER,
             ACCOUNTS.ACCOUNT_NUMBER
        FROM V_ACTIVE_NUMBERS_OUT_CONTRACTS,
             ACCOUNTS
        WHERE V_ACTIVE_NUMBERS_OUT_CONTRACTS.ACCOUNT_ID=ACCOUNTS.ACCOUNT_ID(+)
          AND NVL(ACCOUNTS.DO_AUTO_BLOCK, 0) = 1
          AND NOT EXISTS (SELECT 1
                            FROM AUTO_BLOCKED_PHONE_NO_CONTRACT
                            WHERE AUTO_BLOCKED_PHONE_NO_CONTRACT.PHONE_NUMBER = V_ACTIVE_NUMBERS_OUT_CONTRACTS.PHONE_NUMBER
                              AND AUTO_BLOCKED_PHONE_NO_CONTRACT.BLOCK_DATE_TIME > SYSDATE-7/24/60)
          AND NOT EXISTS (SELECT 1
                            FROM QUEUE_PHONE_REBLOCK
                            WHERE QUEUE_PHONE_REBLOCK.PHONE_NUMBER = V_ACTIVE_NUMBERS_OUT_CONTRACTS.PHONE_NUMBER)
          AND NOT EXISTS (SELECT 1
                            FROM PHONE_NUMBER_BLOCK_DENIED
                            WHERE PHONE_NUMBER_BLOCK_DENIED.PHONE_NUMBER = V_ACTIVE_NUMBERS_OUT_CONTRACTS.PHONE_NUMBER))
    LOOP     
      IF DBMS_LOB.INSTR(ACCESS_DENIED_LIST, rec.PHONE_NUMBER) = 0 THEN
        OPEN C1(rec.PHONE_NUMBER);
        FETCH C1 INTO vCONTR;
        IF C1%NOTFOUND THEN
          LOCK_PH:=LOADER3_pckg.LOCK_PHONE(rec.PHONE_NUMBER, 0,1);        
          IF LOCK_PH IS NOT NULL THEN
            INSERT INTO AUTO_BLOCKED_PHONE_NO_CONTRACT(PHONE_NUMBER, ACCOUNT_NUMBER, BLOCK_DATE_TIME, MANUAL_BLOCK, USER_NAME, NOTE, SPR) 
              VALUES (rec.PHONE_NUMBER, rec.ACCOUNT_NUMBER, SYSDATE, 0, USER, SUBSTR('Ошибка. '||LOCK_PH, 300), ACCESS_DENIED_LIST);
          ELSE 
            INSERT INTO AUTO_BLOCKED_PHONE_NO_CONTRACT(PHONE_NUMBER, ACCOUNT_NUMBER, BLOCK_DATE_TIME, MANUAL_BLOCK, USER_NAME, NOTE, SPR) 
              VALUES (rec.PHONE_NUMBER, rec.ACCOUNT_NUMBER, SYSDATE, 0, USER, NULL, ACCESS_DENIED_LIST);
          END IF;
          COMMIT;
        END IF;
        CLOSE C1;        
        ACCESS_DENIED_LIST:=GET_LIST_PHONE_NO_BL_FROM_SYTE;  
      END IF;   
    END LOOP;
  END IF;  
END; 