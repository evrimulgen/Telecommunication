
  CREATE OR REPLACE PROCEDURE "CORP_MOBILE"."DELETE_FIX_MONTH" (
  pACCOUNT_ID IN INTEGER,
  pFIX_YEAR_MONTH_ID IN INTEGER
  ) IS
--Version=1  
BEGIN   
  DELETE FROM PHONE_BALANCES 
    WHERE PHONE_BALANCES.FIX_YEAR_MONTH_ID=pFIX_YEAR_MONTH_ID;
  DELETE FROM FIX_YEAR_MONTHS 
    WHERE FIX_YEAR_MONTHS.FIX_YEAR_MONTH_ID=pFIX_YEAR_MONTH_ID
      AND FIX_YEAR_MONTHS.ACCOUNT_ID=pACCOUNT_ID;
END; 