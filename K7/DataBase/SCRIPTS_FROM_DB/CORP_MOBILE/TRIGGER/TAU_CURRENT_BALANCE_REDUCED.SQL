
  CREATE OR REPLACE TRIGGER "CORP_MOBILE"."TAU_CURRENT_BALANCE_REDUCED" 
AFTER UPDATE ON IOT_CURRENT_BALANCE
FOR EACH ROW
BEGIN
--#Version=1
  -- Отправка сообщений о снижении баланса в Телетай
  IF :NEW.BALANCE < :OLD.BALANCE THEN
    -- Если баланс снизился
    TT_BALANCE_REDUCED_PCKG.ADD_PHONE_NUMBER(:NEW.PHONE_NUMBER, :NEW.BALANCE);
  END IF;
END;
ALTER TRIGGER "CORP_MOBILE"."TAU_CURRENT_BALANCE_REDUCED" ENABLE