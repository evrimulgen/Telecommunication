
  CREATE OR REPLACE TRIGGER "CORP_MOBILE"."TIU_ACC_PAYMENTS_4_EQUIPMENT" 
BEFORE INSERT OR UPDATE
ON CORP_MOBILE.ACC_PAYMENTS_FOR_EQUIPMENT 
REFERENCING NEW AS New OLD AS Old
FOR EACH ROW
BEGIN
  IF INSERTING THEN
    :NEW.USER_CREATED := USER;
    :NEW.DATE_CREATED := SYSDATE;
  END IF;  
  :NEW.USER_LAST_UPDATED := USER;
  :NEW.DATE_LAST_UPDATED := SYSDATE;
END TIU_ACC_PAYMENTS_4_EQUIPMENT;
ALTER TRIGGER "CORP_MOBILE"."TIU_ACC_PAYMENTS_4_EQUIPMENT" ENABLE