
  CREATE OR REPLACE TRIGGER "CORP_MOBILE"."TIU_ANSWER_TYPES" 
  BEFORE INSERT OR UPDATE ON ANSWER_TYPES FOR EACH ROW 
--#Version=1
BEGIN 
  IF INSERTING THEN 
    IF NVL(:NEW.ANSWER_TYPE_ID, 0) = 0 then 
      :NEW.ANSWER_TYPE_ID := NEW_ANSWER_TYPE_ID; 
    END IF; 
    :NEW.USER_CREATED := USER; 
    :NEW.DATE_CREATED := SYSDATE;
  END IF; 
  :NEW.USER_LAST_UPDATED := USER; 
  :NEW.DATE_LAST_UPDATED := SYSDATE; 
END;
--#end if
ALTER TRIGGER "CORP_MOBILE"."TIU_ANSWER_TYPES" ENABLE