
  CREATE OR REPLACE TRIGGER "CORP_MOBILE"."TIU_BEELINE_TICKET_TYPES" 
  BEFORE INSERT OR UPDATE ON BEELINE_TICKET_TYPES FOR EACH ROW 
--#Version=1
BEGIN 
  IF INSERTING THEN 
    IF NVL(:NEW.TICKET_TYPE_ID, 0) = 0 then 
      :NEW.TICKET_TYPE_ID := NEW_BEELINE_TICKET_TYPE_ID; 
    END IF; 
    :NEW.USER_CREATED := USER; 
    :NEW.DATE_CREATED := SYSDATE;
  END IF; 
  :NEW.USER_LAST_UPDATED := USER; 
  :NEW.DATE_LAST_UPDATED := SYSDATE; 
END;
--#end if
ALTER TRIGGER "CORP_MOBILE"."TIU_BEELINE_TICKET_TYPES" ENABLE