
  CREATE OR REPLACE TRIGGER "CORP_MOBILE"."TIU_BILL_RECALC_ADD_COST" 
--#Version=1
  BEFORE INSERT OR UPDATE ON BILL_RECALC_ADD_COST FOR EACH ROW
BEGIN
  IF UPDATING THEN
    IF (:NEW.CALLS_ALL < :OLD.CALLS_ALL) OR (:NEW.CALLS < :OLD.CALLS) OR (:NEW.SMS < :OLD.SMS) THEN
      :NEW.CALLS_ALL := :OLD.CALLS_ALL;
      :NEW.CALLS := :OLD.CALLS;
      :NEW.SMS := :OLD.SMS;
    END IF;
  END IF;
  :NEW.DATE_CHECK := SYSDATE;
END;
ALTER TRIGGER "CORP_MOBILE"."TIU_BILL_RECALC_ADD_COST" ENABLE