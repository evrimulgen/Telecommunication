
  CREATE OR REPLACE TRIGGER "CORP_MOBILE"."TIU_CALL_SUMMARY" 
  BEFORE INSERT OR UPDATE ON CALL_SUMMARY FOR EACH ROW 
--#Version=1
BEGIN 
  IF INSERTING THEN 
    :NEW.USER_CREATED := USER; 
    :NEW.DATE_CREATED := SYSDATE;
  END IF; 
  :NEW.USER_LAST_UPDATED := USER; 
  :NEW.DATE_LAST_UPDATED := SYSDATE; 
END;
--#end if
ALTER TRIGGER "CORP_MOBILE"."TIU_CALL_SUMMARY" ENABLE