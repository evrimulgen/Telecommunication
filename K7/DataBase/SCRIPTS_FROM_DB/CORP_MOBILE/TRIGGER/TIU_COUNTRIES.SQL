
  CREATE OR REPLACE TRIGGER "CORP_MOBILE"."TIU_COUNTRIES" 
  BEFORE INSERT OR UPDATE ON COUNTRIES FOR EACH ROW
--#Version=1
BEGIN
  IF INSERTING THEN
    IF NVL(:NEW.COUNTRY_ID, 0) = 0 then
      :NEW.COUNTRY_ID := NEW_COUNTRY_ID;
    END IF;
    :NEW.USER_CREATED := USER;
    :NEW.DATE_CREATED := SYSDATE;
  END IF;
  :NEW.USER_LAST_UPDATED := USER;
  :NEW.DATE_LAST_UPDATED := SYSDATE;
END; 
ALTER TRIGGER "CORP_MOBILE"."TIU_COUNTRIES" ENABLE