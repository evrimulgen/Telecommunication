
  CREATE OR REPLACE TRIGGER "CORP_MOBILE"."TIU_CRM_REQUESTS" 
  BEFORE INSERT OR UPDATE ON CRM_REQUESTS FOR EACH ROW
--#Version=1
BEGIN
  IF INSERTING THEN
    IF NVL(:NEW.REQUEST_ID, 0) = 0 then
      :NEW.REQUEST_ID := S_NEW_REQUEST_ID.NEXTVAL;
    END IF;
  END IF;
  :NEW.DATE_LAST_UPDATED:=SYSDATE;
END;
ALTER TRIGGER "CORP_MOBILE"."TIU_CRM_REQUESTS" ENABLE