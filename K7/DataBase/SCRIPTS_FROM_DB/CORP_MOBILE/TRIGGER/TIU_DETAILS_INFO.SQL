
  CREATE OR REPLACE TRIGGER "CORP_MOBILE"."TIU_DETAILS_INFO" 
  BEFORE INSERT OR UPDATE ON DETAILS_INFO FOR EACH ROW
--#Version=1
DECLARE
  PRAGMA AUTONOMOUS_TRANSACTION;
BEGIN
  IF INSERTING THEN
    IF NVL(:NEW.DETAIL_INFO_ID, 0)=0 THEN
      :NEW.DETAIL_INFO_ID:=NEW_DETAIL_INFO_ID;
    END IF;
  END IF;
END; 
ALTER TRIGGER "CORP_MOBILE"."TIU_DETAILS_INFO" ENABLE