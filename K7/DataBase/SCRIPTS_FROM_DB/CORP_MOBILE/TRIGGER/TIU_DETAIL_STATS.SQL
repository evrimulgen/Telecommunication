
  CREATE OR REPLACE TRIGGER "CORP_MOBILE"."TIU_DETAIL_STATS" 
  BEFORE INSERT OR UPDATE ON DETAIL_STATS FOR EACH ROW
--#Version=1
DECLARE
  PRAGMA AUTONOMOUS_TRANSACTION;
BEGIN
  IF INSERTING THEN
    IF NVL(:NEW.DETAIL_STATS_ID, 0)=0 THEN
      :NEW.DETAIL_STATS_ID:=NEW_DETAIL_STATS_ID;
    END IF;
  END IF;
END; 
ALTER TRIGGER "CORP_MOBILE"."TIU_DETAIL_STATS" ENABLE