
  CREATE OR REPLACE TRIGGER "CORP_MOBILE"."TIU_INACTIVE_PHONE_LESS_CONTRA" 
  BEFORE INSERT OR UPDATE ON INACTIVE_PHONE_LESS_CONTRACT FOR EACH ROW
BEGIN
  IF INSERTING THEN
    IF NVL(:NEW.INACTIVE_PHONE_ID, 0) = 0 THEN
      :NEW.INACTIVE_PHONE_ID := NEW_INACTIVE_PHONE_ID;
    END IF;
    :NEW.USER_CREATED := USER;
    :NEW.DATE_CREATED := SYSDATE;
  END IF;
  :NEW.USER_LAST_UPDATED := USER;
  :NEW.DATE_LAST_UPDATED := SYSDATE;
END;

ALTER TRIGGER "CORP_MOBILE"."TIU_INACTIVE_PHONE_LESS_CONTRA" ENABLE