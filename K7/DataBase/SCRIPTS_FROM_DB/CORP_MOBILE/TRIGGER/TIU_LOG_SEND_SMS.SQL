
  CREATE OR REPLACE TRIGGER "CORP_MOBILE"."TIU_LOG_SEND_SMS" 
  BEFORE INSERT OR UPDATE ON LOG_SEND_SMS FOR EACH ROW
--#Version=5
DECLARE
  PRAGMA AUTONOMOUS_TRANSACTION;
BEGIN   
  IF INSERTING THEN
    :NEW.DATE_SEND:=SYSDATE;
    IF NVL(:NEW.YEAR_MONTH, 0)=0 THEN
      :NEW.YEAR_MONTH:=TO_NUMBER(TO_CHAR(SYSDATE, 'YYYYMM'));
    END IF;
  END IF;
END; 
ALTER TRIGGER "CORP_MOBILE"."TIU_LOG_SEND_SMS" ENABLE