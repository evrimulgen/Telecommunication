
  CREATE OR REPLACE TRIGGER "CORP_MOBILE"."TIU_MASK_BEAUTY" 
  BEFORE INSERT OR UPDATE ON MASK_BEAUTY FOR EACH ROW
BEGIN
  IF INSERTING THEN
    IF NVL(:NEW.MASK_BEAUTY_ID, 0) = 0 THEN
      :NEW.MASK_BEAUTY_ID := NEW_MASK_BEAUTY_ID;
    END IF;
    :NEW.USER_CREATED := USER;
    :NEW.DATE_CREATED := SYSDATE;
  END IF;
  :NEW.USER_LAST_UPDATED := USER;
  :NEW.DATE_LAST_UPDATED := SYSDATE;
END;

ALTER TRIGGER "CORP_MOBILE"."TIU_MASK_BEAUTY" ENABLE