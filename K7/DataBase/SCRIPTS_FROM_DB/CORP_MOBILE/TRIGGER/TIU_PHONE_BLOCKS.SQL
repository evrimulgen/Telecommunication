
  CREATE OR REPLACE TRIGGER "CORP_MOBILE"."TIU_PHONE_BLOCKS" 
  BEFORE INSERT OR UPDATE ON PHONE_BLOCKS FOR EACH ROW
--#Version=1
BEGIN
  IF INSERTING THEN
    IF NVL(:NEW.PHONE_BLOCK_ID, 0) = 0 then
      :NEW.PHONE_BLOCK_ID := NEW_PHONE_BLOCK_ID;
    END IF;
    :NEW.USER_CREATED := USER;
    :NEW.DATE_CREATED := SYSDATE;
  END IF;
  :NEW.USER_LAST_UPDATED := USER;
  :NEW.DATE_LAST_UPDATED := SYSDATE;
END; 
ALTER TRIGGER "CORP_MOBILE"."TIU_PHONE_BLOCKS" ENABLE