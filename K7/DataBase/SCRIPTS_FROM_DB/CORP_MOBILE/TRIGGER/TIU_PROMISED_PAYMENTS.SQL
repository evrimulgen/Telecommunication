
  CREATE OR REPLACE TRIGGER "CORP_MOBILE"."TIU_PROMISED_PAYMENTS" 
  BEFORE INSERT OR UPDATE ON PROMISED_PAYMENTS FOR EACH ROW 
--#Version=1
BEGIN 
  IF INSERTING THEN 
    :NEW.USER_CREATED := USER; 
    :NEW.DATE_CREATED := SYSDATE; 
    :NEW.PROMISED_SUM_ORIGINAL:=:NEW.PROMISED_SUM;
  END IF;   
  :NEW.USER_LAST_UPDATED := USER; 
  :NEW.DATE_LAST_UPDATED := SYSDATE; 
END; 
ALTER TRIGGER "CORP_MOBILE"."TIU_PROMISED_PAYMENTS" ENABLE