
  CREATE OR REPLACE TRIGGER "CORP_MOBILE"."TIU_QUEUE_PHONE_REBLOCK" 
  BEFORE INSERT OR UPDATE ON QUEUE_PHONE_REBLOCK FOR EACH ROW 
