
  CREATE OR REPLACE TRIGGER "CORP_MOBILE"."TIU_REF_TEXT" 
  BEFORE INSERT OR UPDATE ON REF_TEXT FOR EACH ROW
--#Version=1
BEGIN
  IF INSERTING THEN
    :NEW.REF_TEXT_ID := S_NEW_REF_TEXT_ID.NEXTVAL;
  END IF;
  
  IF UPDATING THEN
    :NEW.USER_UPDATED := USER;
    :NEW.DATE_UPDATED := SYSDATE;
  END IF;
END;
ALTER TRIGGER "CORP_MOBILE"."TIU_REF_TEXT" ENABLE