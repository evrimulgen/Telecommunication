
  CREATE OR REPLACE TRIGGER "CORP_MOBILE"."TIU_SEND_SMS_QUERY" 
  BEFORE INSERT OR UPDATE ON SEND_SMS_QUERY FOR EACH ROW
--#Version=1
DECLARE
  PRAGMA AUTONOMOUS_TRANSACTION;
BEGIN
  IF INSERTING THEN
    IF NVL(:NEW.SMS_ID, 0) = 0 THEN
      :NEW.SMS_ID := NEW_SMS_ID;
    END IF;
  END IF;
  :NEW.DATE_SEND := SYSDATE;
END; 
ALTER TRIGGER "CORP_MOBILE"."TIU_SEND_SMS_QUERY" ENABLE