
  CREATE OR REPLACE TRIGGER "CORP_MOBILE"."TIU_TARIFF_OPTION_GROUP_COSTS" 
  BEFORE INSERT OR UPDATE ON TARIFF_OPTION_GROUP_COSTS FOR EACH ROW
--#Version=1
BEGIN
  IF INSERTING THEN
    IF NVL(:NEW.TARIFF_OPT_GROUP_COST_ID, 0) = 0 then
      :NEW.TARIFF_OPT_GROUP_COST_ID := NEW_TARIFF_OPT_GROUP_COST_ID;
    END IF;
    :NEW.USER_CREATED := USER;
    :NEW.DATE_CREATED := SYSDATE;
  END IF;
  :NEW.USER_LAST_UPDATED := USER;
  :NEW.DATE_LAST_UPDATED := SYSDATE;
END; 
ALTER TRIGGER "CORP_MOBILE"."TIU_TARIFF_OPTION_GROUP_COSTS" ENABLE