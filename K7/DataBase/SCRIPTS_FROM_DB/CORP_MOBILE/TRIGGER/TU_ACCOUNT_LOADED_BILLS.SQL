
  CREATE OR REPLACE TRIGGER "CORP_MOBILE"."TU_ACCOUNT_LOADED_BILLS" 
BEFORE UPDATE
ON ACCOUNT_LOADED_BILLS
REFERENCING NEW AS NEW OLD AS OLD
FOR EACH ROW
DECLARE
   PRAGMA AUTONOMOUS_TRANSACTION;
BEGIN
  IF UPDATING AND :OLD.LOAD_BILL_IN_BALANCE <>:NEW.LOAD_BILL_IN_BALANCE THEN  
    p_new_bill_balance(:OLD.ACCOUNT_ID, :OLD.YEAR_MONTH); 
  END IF;          
END;
ALTER TRIGGER "CORP_MOBILE"."TU_ACCOUNT_LOADED_BILLS" ENABLE