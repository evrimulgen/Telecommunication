
  CREATE OR REPLACE FORCE VIEW "CORP_MOBILE"."V_ACCOUNT_PHONE_LIST" ("LOGIN", "ACCOUNT_ID", "PHONE_NUMBER", "TARIFF_CODE", "PHONE_IS_ACTIVE", "CONSERVATION", "SYSTEM_BLOCK") AS 
  SELECT AC.LOGIN,
         AP.ACCOUNT_ID,
         AP.PHONE_NUMBER,
         AP.CELL_PLAN_CODE TARIFF_CODE,
         CASE
           WHEN AP.PHONE_IS_ACTIVE = 1 THEN 'Акт.'
           ELSE 'Блок.'
         END PHONE_IS_ACTIVE,         
         CASE
           WHEN AP.CONSERVATION = 1 THEN 'Сохран.'
           ELSE NULL
         END CONSERVATION,         
         CASE
           WHEN AP.SYSTEM_BLOCK = 1 THEN 'Бл. за мош.'
           ELSE NULL
         END SYSTEM_BLOCK
    FROM DB_LOADER_ACCOUNT_PHONES AP,
         ACCOUNTS AC
    WHERE AP.YEAR_MONTH = (SELECT MAX(YEAR_MONTH)
                             FROM DB_LOADER_ACCOUNT_PHONES)
      AND AP.ACCOUNT_ID = AC.ACCOUNT_ID(+) 