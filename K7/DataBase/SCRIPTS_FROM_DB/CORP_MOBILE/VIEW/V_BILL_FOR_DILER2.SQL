
  CREATE OR REPLACE FORCE VIEW "CORP_MOBILE"."V_BILL_FOR_DILER2" ("ACCOUNT_ID", "YEAR_MONTH", "PHONE_NUMBER", "BILL_SUM_ORIGIN", "BILL_SUM", "DISCOUNT_VALUE", "DILER_PAYMENT", "DILER_PAYMENT_FULL", "SUBSCRIBER_PAYMENT_NEW", "SUBSCRIBER_PAYMENT_OLD", "SUBSCRIBER_PAYMENT_ADD_OLD", "OPTION_COST_DILER", "OPTION_COST_DILER_BEELINE", "OPTION_COST_FULL", "TARIFF_NAME", "TAIL", "CHECK_LONG_PLUS_BALANCE", "INSTALLMENT_PAYMENT_SUM", "DILER_SUMM_OLD_MONTH_IN_MINUS", "DILER_FULL_SUM", "BEELINE_FULL_SUM", "DILER_ITOG_SUM", "CHECK_LONG_PLUS_BALANCE2") AS 
  SELECT 
-- Version=5
         V1."ACCOUNT_ID",V1."YEAR_MONTH",V1."PHONE_NUMBER",V1."BILL_SUM_ORIGIN",V1."BILL_SUM",V1."DISCOUNT_VALUE",V1."DILER_PAYMENT",V1."DILER_PAYMENT_FULL",V1."SUBSCRIBER_PAYMENT_NEW",V1."SUBSCRIBER_PAYMENT_OLD",V1."SUBSCRIBER_PAYMENT_ADD_OLD",V1."OPTION_COST_DILER",V1."OPTION_COST_DILER_BEELINE",V1."OPTION_COST_FULL",V1."TARIFF_NAME",V1."TAIL",V1."CHECK_LONG_PLUS_BALANCE",V1."INSTALLMENT_PAYMENT_SUM",V1."DILER_SUMM_OLD_MONTH_IN_MINUS",
         CASE
           WHEN V1.SUBSCRIBER_PAYMENT_NEW+V1.DILER_PAYMENT+V1.OPTION_COST_DILER < V1.BILL_SUM
             THEN V1.SUBSCRIBER_PAYMENT_NEW+V1.DILER_PAYMENT+V1.OPTION_COST_DILER 
                  + V1.INSTALLMENT_PAYMENT_SUM + V1.DILER_SUMM_OLD_MONTH_IN_MINUS
           ELSE V1.BILL_SUM
         END DILER_FULL_SUM,
         V1.SUBSCRIBER_PAYMENT_OLD+V1.DILER_PAYMENT+V1.OPTION_COST_DILER_BEELINE BEELINE_FULL_SUM,
         CASE
           WHEN V1.SUBSCRIBER_PAYMENT_NEW+V1.DILER_PAYMENT+V1.OPTION_COST_DILER < V1.BILL_SUM
             THEN V1.SUBSCRIBER_PAYMENT_NEW+V1.DILER_PAYMENT+V1.OPTION_COST_DILER
                  + V1.INSTALLMENT_PAYMENT_SUM + V1.DILER_SUMM_OLD_MONTH_IN_MINUS
           ELSE V1.BILL_SUM + V1.INSTALLMENT_PAYMENT_SUM + V1.DILER_SUMM_OLD_MONTH_IN_MINUS
         END
          + CASE
              WHEN (V1.CHECK_LONG_PLUS_BALANCE=0 and v1.BILL_SUM_ORIGIN>0) THEN V1.TAIL
              ELSE 0
            END DILER_ITOG_SUM,
         CASE
           WHEN V1.CHECK_LONG_PLUS_BALANCE=0 THEN '-'
           ELSE '+'
         END CHECK_LONG_PLUS_BALANCE2
    FROM V_BILL_FOR_DILER_V1 V1 