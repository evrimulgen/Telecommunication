
  CREATE OR REPLACE FORCE VIEW "CORP_MOBILE"."V_BILL_MN_ROUMING_FOR_GROUP" ("ACCOUNT_ID", "YEAR_MONTH", "PHONE_NUMBER", "DATE_BEGIN", "DATE_END", "ROUMING_COUNTRY", "ROUMING_CALLS", "ROUMING_GPRS", "ROUMING_SMS", "COMPANY_TAX", "ROUMING_SUM", "CONTRACT_ID", "CONTRACT_NUM", "CONTRACT_DATE", "GROUP_ID") AS 
  SELECT
  MN.ACCOUNT_ID,
  MN.YEAR_MONTH,
  MN.PHONE_NUMBER,
  MN.DATE_BEGIN,
  MN.DATE_END,
  MN.ROUMING_COUNTRY,
  MN.ROUMING_CALLS,
  MN.ROUMING_GPRS,
  MN.ROUMING_SMS,
  MN.COMPANY_TAX,
  MN.ROUMING_SUM,
  C.CONTRACT_ID,
  C.CONTRACT_NUM,
  C.CONTRACT_DATE,
  C.GROUP_ID
FROM DB_LOADER_FULL_BILL_MN_ROUMING MN, V_CONTRACTS C
WHERE MN.PHONE_NUMBER = C.PHONE_NUMBER_FEDERAL
  AND C.CONTRACT_DATE < ADD_MONTHS(TO_DATE(MN.YEAR_MONTH||'01', 'yyyymmdd'), 1)
  AND (C.CONTRACT_CANCEL_DATE IS NULL OR C.CONTRACT_CANCEL_DATE >= TO_DATE(MN.YEAR_MONTH||'01', 'yyyymmdd'))
  AND C.GROUP_ID IS NOT NULL
