CREATE OR REPLACE FUNCTION CRM_GET_PARAM_VALUE 
 (
 paramname IN VARCHAR2
 ) RETURN VARCHAR2 IS 
BEGIN
 return MS_params.GET_PARAM_VALUE(paramname);
END;
/

GRANT EXECUTE ON CRM_GET_PARAM_VALUE TO CRM_USER;
CREATE SYNONYM CRM_USER.GET_PARAM_VALUE FOR CRM_GET_PARAM_VALUE;

-- пример использования GET_PARAM_VALUE('SEND_SMS_CREATE_REQUEST')='1'
