CREATE TABLE CRM_HELP(
  HELP_ID INTEGER,
  HELP_PATH VARCHAR2(150),
  TITLE VARCHAR2(250),
  CONTENT LONG,
  DATE_CREATED DATE,
  USER_CREATED       VARCHAR2(30 CHAR),
  USER_LAST_UPDATED  VARCHAR2(30 CHAR),
  DATE_LAST_UPDATED  DATE);

CREATE SEQUENCE S_NEW_HELP_ID;

CREATE OR REPLACE TRIGGER TIU_CRM_HELP
  BEFORE INSERT OR UPDATE ON CRM_HELP FOR EACH ROW
--#Version=1
BEGIN
  IF INSERTING THEN
    IF NVL(:NEW.HELP_ID, 0) = 0 then
      :NEW.HELP_ID := S_NEW_HELP_ID.NEXTVAL;
    END IF;
    :NEW.DATE_CREATED:=SYSDATE;
  END IF;
  :NEW.DATE_LAST_UPDATED:=SYSDATE;
END;

GRANT SELECT, INSERT, DELETE, UPDATE ON CRM_HELP TO CRM_USER;
CREATE SYNONYM CRM_USER.HELP FOR CRM_HELP;
