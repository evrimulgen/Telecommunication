
  CREATE OR REPLACE TRIGGER "SIM_TRADE"."TIU_CONTRACT_CANCEL_TYPES" 
  BEFORE INSERT OR UPDATE ON CONTRACT_CANCEL_TYPES FOR EACH ROW
--#Version=1
BEGIN
  IF INSERTING THEN
    IF NVL(:NEW.CONTRACT_CANCEL_TYPE_ID, 0) = 0 then
      :NEW.CONTRACT_CANCEL_TYPE_ID := NEW_CONTRACT_CANCEL_TYPE_ID;
    END IF;
    :NEW.USER_CREATED := USER;
    :NEW.DATE_CREATED := SYSDATE;
  END IF;
  :NEW.USER_LAST_UPDATED := USER;
  :NEW.DATE_LAST_UPDATED := SYSDATE;
END; 

ALTER TRIGGER "SIM_TRADE"."TIU_CONTRACT_CANCEL_TYPES" ENABLE