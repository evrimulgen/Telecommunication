
  CREATE OR REPLACE TRIGGER "SIM_TRADE"."TIU_QUEUE_PHONE_REBLOCK" 
  BEFORE INSERT OR UPDATE ON QUEUE_PHONE_REBLOCK FOR EACH ROW 
--#Version=1
BEGIN 
  IF INSERTING THEN 
    IF NVL(:NEW.QUEUE_PHONE_REBLOCK_ID, 0) = 0 then 
      :NEW.QUEUE_PHONE_REBLOCK_ID := NEW_QUEUE_PHONE_REBLOCK_ID; 
    END IF; 
    IF :NEW.DATE_ACTIV IS NULL then 
      :NEW.DATE_ACTIV := SYSDATE; 
    END IF; 
    IF :NEW.DATE_CREATED IS NULL then 
      :NEW.DATE_CREATED := SYSDATE; 
    END IF; 
  END IF;
END; 

ALTER TRIGGER "SIM_TRADE"."TIU_QUEUE_PHONE_REBLOCK" ENABLE