
  CREATE OR REPLACE TRIGGER "SIM_TRADE"."TIU_SERVICES" 
  BEFORE INSERT OR UPDATE ON SERVICES_ FOR EACH ROW
--#Version=1
BEGIN
  IF INSERTING THEN
    IF NVL(:NEW.SERVICE_ID, 0) = 0 then
      :NEW.SERVICE_ID := NEW_SERVICE_ID;
    END IF;
    :NEW.USER_CREATED := USER;
    :NEW.DATE_CREATED := SYSDATE;
  END IF;
  :NEW.USER_LAST_UPDATED := USER;
  :NEW.DATE_LAST_UPDATED := SYSDATE;
END; 

ALTER TRIGGER "SIM_TRADE"."TIU_SERVICES" ENABLE