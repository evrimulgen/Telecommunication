
  CREATE OR REPLACE FORCE VIEW "SIM_TRADE"."V_BILL_FINANCE_FOR_CLIENT_DET" ("LOGIN", "ACCOUNT_ID", "YEAR_MONTH", "PHONE_NUMBER", "BILL_SUM_NEW", "ABON_TP_NEW", "ABON_ADD_NEW", "DISCOUNT_NEW", "SINGLE_PAYMENTS_NEW", "DISCOUNT_CALL", "DISCOUNT_SMS_PLUS", "SINGLE_CHANGE_TARIFF", "SINGLE_VIEW_BLACK_LIST", "SINGLE_CORRECTION_ROUMING", "SINGLE_INTRA_WEB", "SINGLE_PENALTI", "SINGLE_TURN_ON_SERV", "SINGLE_OTHER", "CALLS_COUNTRY", "CALLS_SITY", "CALLS_LOCAL", "CALLS_SMS_MMS", "CALLS_GPRS", "CALLS_RUS_RPP", "ROUMING_NATIONAL", "ROUMING_INTERNATIONAL") AS 
  SELECT ACCOUNTS.LOGIN,
         FB.ACCOUNT_ID,
         FB.YEAR_MONTH,
         FB.PHONE_NUMBER,
         V.BILL_SUM_NEW,
         V.ABON_TP_NEW,
         V.ABON_ADD_NEW,
         V.DISCOUNT_NEW,
         V.SINGLE_PAYMENTS_NEW,
         FB.DISCOUNT_CALL,
         FB.DISCOUNT_SMS_PLUS,
         FB.SINGLE_CHANGE_TARIFF,
         FB.SINGLE_VIEW_BLACK_LIST,
         FB.SINGLE_CORRECTION_ROUMING,
         FB.SINGLE_INTRA_WEB,
         FB.SINGLE_PENALTI, 
         FB.SINGLE_TURN_ON_SERV,
         FB.SINGLE_OTHER,
         FB.CALLS_COUNTRY,
         FB.CALLS_SITY,
         FB.CALLS_LOCAL,
         FB.CALLS_SMS_MMS,
         FB.CALLS_GPRS,
         FB.CALLS_RUS_RPP,
         FB.ROUMING_NATIONAL,
         FB.ROUMING_INTERNATIONAL
    FROM DB_LOADER_FULL_FINANCE_BILL FB,
         V_BILL_FINANCE_FOR_CLIENTS V,
         ACCOUNTS
    WHERE FB.ACCOUNT_ID = V.ACCOUNT_ID(+)
      AND FB.YEAR_MONTH = V.YEAR_MONTH(+)
      AND FB.PHONE_NUMBER = V.PHONE_NUMBER(+)
      AND FB.ACCOUNT_ID = ACCOUNTS.ACCOUNT_ID(+) 