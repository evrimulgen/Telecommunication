
  CREATE OR REPLACE FORCE VIEW "SIM_TRADE"."V_CONTRACT_PRINT" ("SURNAME", "NAME", "PATRONYMIC", "BDATE", "PASSPORT_SER", "PASSPORT_NUM", "PASSPORT_GET", "PASSPORT_DATE", "COUNTRY_CITIZEN", "COUNTRY_NAME", "REGION_NAME", "CITY_NAME", "STREET_NAME", "HOUSE", "KORPUS", "APARTMENT", "CODE_WORD", "CONTACT_INFO", "OPERATOR_NAME", "TARIFF_NAME", "CONNECT_PRICE", "ADVANCE_PAYMENT", "TOTAL", "CONTRACT_ID", "CONTRACT_NUM", "CONTRACT_DATE", "FILIAL_ID", "OPERATOR_ID", "PHONE_NUMBER_FEDERAL", "PHONE_NUMBER_CITY", "PHONE_NUMBER_TYPE", "TARIFF_ID", "SIM_NUMBER", "SERVICE_ID", "DISCONNECT_LIMIT", "CONFIRMED", "USER_CREATED", "DATE_CREATED", "USER_LAST_UPDATED", "DATE_LAST_UPDATED", "ABONENT_ID", "RECEIVED_SUM", "START_BALANCE", "GOLD_NUMBER_SUM", "HAND_BLOCK", "CONNECT_LIMIT") AS 
  SELECT
--#Version=1 
      AB.SURNAME, AB.NAME, AB.PATRONYMIC, AB.BDATE
      ,AB.PASSPORT_SER, AB.PASSPORT_NUM, AB.PASSPORT_GET, AB.PASSPORT_DATE
      ,СNT_CITIZEN.COUNTRY_NAME COUNTRY_CITIZEN
      ,CN.COUNTRY_NAME
      ,RG.REGION_NAME
      ,AB.CITY_NAME
      ,AB.STREET_NAME
      ,AB.HOUSE
      ,AB.KORPUS
      ,AB.APARTMENT
      ,AB.CODE_WORD
      ,AB.CONTACT_INFO
      ,O.OPERATOR_NAME
      ,TR.TARIFF_NAME
      ,TR.CONNECT_PRICE
      ,TR.ADVANCE_PAYMENT
      ,C.RECEIVED_SUM TOTAL
      ,C."CONTRACT_ID",C."CONTRACT_NUM",C."CONTRACT_DATE",C."FILIAL_ID",C."OPERATOR_ID",C."PHONE_NUMBER_FEDERAL",C."PHONE_NUMBER_CITY",C."PHONE_NUMBER_TYPE",C."TARIFF_ID",C."SIM_NUMBER",C."SERVICE_ID",C."DISCONNECT_LIMIT",C."CONFIRMED",C."USER_CREATED",C."DATE_CREATED",C."USER_LAST_UPDATED",C."DATE_LAST_UPDATED",C."ABONENT_ID",C."RECEIVED_SUM",C."START_BALANCE",C."GOLD_NUMBER_SUM",C."HAND_BLOCK",C."CONNECT_LIMIT"
  FROM CONTRACTS C
      ,ABONENTS AB
      ,COUNTRIES СNT_CITIZEN
      ,COUNTRIES CN
      ,REGIONS RG
      ,OPERATORS O
      ,TARIFFS TR
 WHERE C.ABONENT_ID = AB.ABONENT_ID (+)
   AND AB.CITIZENSHIP = СNT_CITIZEN.COUNTRY_ID (+)
   AND AB.COUNTRY_ID = CN.COUNTRY_ID (+) 
   AND AB.REGION_ID = RG.REGION_ID (+)
   AND C.OPERATOR_ID = O.OPERATOR_ID (+)
   AND C.TARIFF_ID = TR.TARIFF_ID (+) 