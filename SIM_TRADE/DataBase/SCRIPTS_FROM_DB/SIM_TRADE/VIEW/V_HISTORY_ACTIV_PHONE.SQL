
  CREATE OR REPLACE FORCE VIEW "SIM_TRADE"."V_HISTORY_ACTIV_PHONE" ("PHONE_NUMBER", "BEGIN_YM", "END_YM") AS 
  SELECT H.PHONE_NUMBER,
          TO_CHAR (H.BEGIN_DATE, 'yyyymm') BEGIN_YM,
          TO_CHAR (H.END_DATE, 'yyyymm') END_YM
     FROM DB_LOADER_ACCOUNT_PHONE_HISTS H
    WHERE H.PHONE_IS_ACTIVE = 1