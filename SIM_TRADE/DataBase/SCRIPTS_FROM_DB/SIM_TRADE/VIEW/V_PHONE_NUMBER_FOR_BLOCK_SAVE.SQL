
  CREATE OR REPLACE FORCE VIEW "SIM_TRADE"."V_PHONE_NUMBER_FOR_BLOCK_SAVE" ("ACCOUNT_ID", "YEAR_MONTH", "PHONE_NUMBER", "BEGIN_DATE", "BALANCE", "FIO") AS 
  SELECT DB_LOADER_ACCOUNT_PHONES.ACCOUNT_ID,
         DB_LOADER_ACCOUNT_PHONES.YEAR_MONTH,
         DB_LOADER_ACCOUNT_PHONES.PHONE_NUMBER,
         DB_LOADER_ACCOUNT_PHONE_HISTS.BEGIN_DATE,
         V_ABONENT_BALANCES.BALANCE,
         V_ABONENT_BALANCES.SURNAME
          || ' '
          || V_ABONENT_BALANCES.NAME
          || ' '
          || V_ABONENT_BALANCES.PATRONYMIC FIO
    FROM DB_LOADER_ACCOUNT_PHONES,
         DB_LOADER_ACCOUNT_PHONE_HISTS,
         V_ABONENT_BALANCES       
    WHERE DB_LOADER_ACCOUNT_PHONES.YEAR_MONTH=TO_NUMBER(TO_CHAR(SYSDATE,'YYYYMM'))
      AND DB_LOADER_ACCOUNT_PHONES.PHONE_NUMBER=DB_LOADER_ACCOUNT_PHONE_HISTS.PHONE_NUMBER(+)
      AND DB_LOADER_ACCOUNT_PHONES.PHONE_IS_ACTIVE=0
      AND DB_LOADER_ACCOUNT_PHONES.CONSERVATION=0   
      AND DB_LOADER_ACCOUNT_PHONE_HISTS.END_DATE>SYSDATE
      AND DB_LOADER_ACCOUNT_PHONE_HISTS.BEGIN_DATE<SYSDATE-1
      AND DB_LOADER_ACCOUNT_PHONES.PHONE_NUMBER=V_ABONENT_BALANCES.PHONE_NUMBER_FEDERAL(+)
    ORDER BY DB_LOADER_ACCOUNT_PHONE_HISTS.BEGIN_DATE ASC 