CREATE OR REPLACE FUNCTION NEW_ACCOUNT_ID RETURN NUMBER IS
--#Version=1
  vRES NUMBER;
BEGIN
  SELECT S_NEW_ACCOUNT_ID.NEXTVAL
  INTO vRES
  FROM DUAL;
  RETURN vRES;
END;

